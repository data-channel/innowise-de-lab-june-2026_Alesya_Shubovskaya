"""
Скрипт для расчёта геометрических параметров новых квадратных зон хранения.

Назначение: рассчитывает длину ограждения (периметр)
для квадратной зоны хранения на основе известной площади.

Автор: Алеся Шубовская
Версия: 1.0
Дата: 2025-07-24
"""

import math

# Запрос идентификатора зоны
zone_id = input("Введите идентификатор зоны (например, Zone-A1): ")

# Общая площадь зоны в квадратных метрах (в коде)
area_total = 225

# Расчёт длины одной стороны
side_length = math.sqrt(area_total)

# Вывод итогового отчёта
print(f"Расчёт для объекта: {zone_id}")
print(f"Длина стены зоны: {side_length} м")

